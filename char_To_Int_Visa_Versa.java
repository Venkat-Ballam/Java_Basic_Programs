// Converting Characters to int visa versa 

class Main {
    public static void main(String[] args) {
    
        char value1 = 'A';
        
        System.out.println(value1);
        System.out.println((char)(value1+32));
        System.out.println();
        
        char value2 = 'G';
        
        System.out.println(value2);
        System.out.println((char)(value2+32));
        System.out.println();
        
        char value3 = 'x';
        
        System.out.println(value3);
        System.out.println((char)(value3-32));
    }
}